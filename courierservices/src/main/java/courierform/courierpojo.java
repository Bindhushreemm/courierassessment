package courierform;

public class courierpojo {
 String fullname;
 String phono;
 String city;
 String darea;
 String pgrams;
public String getFullname() {
	return fullname;
}
public void setFullname(String fullname) {
	this.fullname = fullname;
}
public String getPhono() {
	return phono;
}
public void setPhono(String phono) {
	this.phono = phono;
}
public String getCity() {
	return city;
}
public void setCity(String city) {
	this.city = city;
}
public String getDarea() {
	return darea;
}
public void setDarea(String darea) {
	this.darea = darea;
}
public String getPgrams() {
	return pgrams;
}
public void setPgrams(String pgrams) {
	this.pgrams = pgrams;
}
}
